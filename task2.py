while True:
    try:
        user_input = input("Пожалуйста, введите число: ")
        number = int(user_input)
        print(f"Спасибо! Вы ввели число: {number}")
        break  # Выход из цикла, если ввод корректен
    except ValueError:
        print("Ошибка: Введено не число. Попробуйте снова.")

# Дальнейшие действия после ввода правильного числа
print("Программа завершена.")

# 1. Используем цикл while True, чтобы запросить ввод пользователя и продолжать этот процесс до тех пор, пока пользователь не введет корректное число.

# 2. Внутри цикла while есть блок try-except. Мы запрашиваем ввод пользователя с помощью input и пытаемся сконвертировать его в целое число с помощью int(user_input).

# 3. Если ввод пользователя не может быть сконвертирован в целое число (возникает исключение ValueError), 
#     перехватываем это исключение в блоке except и выводим сообщение об ошибке, предлагая пользователю ввести число еще раз.

# 4. Когда пользователь вводит корректное число, мы выводим сообщение и выходим из цикла с помощью break.

# 5. В конце скрипта выводится сообщение о завершении программы.

def получить_значение(словарь, ключ):
    try:
        значение = словарь[ключ]
        return значение
    except KeyError:
        print(f"Ошибка: Ключ '{ключ}' не найден в словаре.")
        return None

# Пример использования
мой_словарь = {'a': 1, 'b': 2, 'c': 3}
искомый_ключ = 'd'

результат = получить_значение(мой_словарь, искомый_ключ)
if результат is not None:
    print(f"Значение для ключа '{искомый_ключ}': {результат}")
else:
    print("Значение не найдено из-за ошибки")


# 1. Определяем функцию получить_значение, которая принимает словарь и ключ в качестве параметров.

# 2. Внутри функции есть блок try-except. Мы пытаемся получить значение из словаря с помощью указанного ключа. Если ключ существует, мы возвращаем соответствующее значение.

# 3. Если ключ отсутствует в словаре и возникает исключение KeyError, мы перехватываем его в блоке except, выводим сообщение об ошибке и возвращаем None.

# 4. Затем мы вызываем функцию получить_значение с заданным словарем и ключом и проверяем возвращенное значение. Если результат не равен None, мы выводим его на экран. В противном случае выводим сообщение об ошибке.

def печать_индекса(список, индекс):
    try:
        элемент = список[индекс]
        print("Элемент с индексом", индекс, ":", элемент)
    except IndexError:
        print("Ошибка: Индекс выходит за границы списка")
    except TypeError:
        print("Ошибка: Индекс должен быть числом")

# Пример
мой_список = [1, 2, 3, 4, 5]

печать_индекса(мой_список, 2)  # Печатает элемент с индексом 2: 3
печать_индекса(мой_список, 10)  # Печатает ошибку: Индекс выходит за границы списка
печать_индекса(мой_список, "abc")  # Печатает ошибку: Индекс должен быть числом


# 1. Создается функция печать_индекса, которая принимает два аргумента: список и индекс.
# 2. Внутри функции используется конструкция try-except для обработки исключений.
# 3. Если индекс находится в пределах допустимых значений, то элемент списка с заданным индексом печатается на экран.
# 4. Если индекс выходит за границы списка, возникает исключение IndexError и выводится соответствующее сообщение об ошибке.
# 5. Если передан не числовой индекс, возникает исключение TypeError и выводится соответствующее сообщение об ошибке.

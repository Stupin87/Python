
def division(x, y):
    try:
        result = x / y
        return result
    except ZeroDivisionError:
        print("Ошибка: Деление на ноль!")
        return None

# Пример 
num1 = 10
num2 = 2

# Вызовем функцию division
result = division(num1, num2)

# Проверим результат и выведем его на экран
if result is not None:
    print(f"Результат деления: {result}")
else:
    print("Деление не выполнено из-за ошибки")
    
# 1. Определили функцию division, которая принимает два числа x и y в качестве параметров.

# 2. Внутри функции мы используем блок try-except. В блоке try мы выполняем деление x на y и сохраняем результат в переменной result.

# 3. Если при выполнении деления возникает ошибка деления на ноль (ZeroDivisionError), мы перехватываем эту ошибку в блоке except, выводим сообщение об ошибке и возвращаем None.

# 4. Затем мы вызываем функцию division с числами num1 и num2 и проверяем возвращенное значение. Если результат не равен None, мы выводим его на экран. В противном случае выводим сообщение об ошибке.

